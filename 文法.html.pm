◊h2{强调句的应用}

◊p{It ◊u{is/was} ... ◊u{that/who} ...}

◊p{[NCE4.5]◊i{If} there is one ◊i{-- which I take leave to doubt -- then it is} older people ◊i{who} create it, not the young themselves.}

◊p{if、then和强调句形成逻辑“如果...那也是...”}

