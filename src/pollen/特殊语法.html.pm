◊h3{非谓语动词}

◊lst{
ul
不定式：◊b{一般来说}表示要做。
分词：表示正在或已做。◊lst{
ul
doing:主动
done:被动
}
}

◊lst{
ul
Trying to explain the situation, she said 'It's only me'.=She tried to explain the situation and said 'It's only me'.但前者更具侧重点。
[NCE3.1]The hunt for the puma began in a small village where a woman ◊b{picking blackberries} saw 'a large cat' only five yards away from her.
}

◊p{当然非谓语动词不仅可以作状语，也可以充当定语、补语、表语等多种成分。以下是几个例子。}

◊h4{不定式作表语}

◊p{不定式作表语若前面出现了实义动词“do”，则常去掉“to”，例如：}

◊lst{
ul
All I have to ◊b{do} is ◊b{dream}.
The best thing you can ◊b{do} now is ◊b{write} her an apology.
}

◊h4{补足语}

◊p{补足语是一类补足主语或宾语意义的句子成分。虽然在语法上不属于主宾，但在逻辑上构成主宾。}

◊h5{不定式作主语补足语}

◊p{sth be ◊i{adj} to do=to do sth be ◊i{adj}}

◊p{主动表被动。但是并不普适。}

◊p{The government not only has to do something, it must be seen to be doing something.政府不仅必须采取措施，而且必须让人们知道它在采取措施。}

◊h5{非谓语动词作宾补}

◊p{过去分词作宾语补足语。}

◊p{[NCE4.4]kept so many old newspapers ◊u{locked away there}}

◊p{lock sth up/away把...锁好}

◊p{不定式作宾语补足语。}

◊p{I see you ◊b{put} the key in your pocket.我看见你把钥匙放进口袋里。}

◊h3{动名词}

◊p{动名词和现在分词的区别在于分词不充当名词成分。基于这种区别，动名词也有其独特的用法。}

◊tbl{
动名词的时态和语态
时态/语态@主动@被动
一般时@doing@being done
完成时@having done@having been done
}

◊lst{
ul
一般时◊b{不强调}先后顺序，因而一般可通用。
完成时强调该动作先发生。
}

◊lst{
ul
After gazing at the display for several minutes, Mr.Taylor went back to his shop.在盯着陈列看了几分钟后，泰勒先生回到了他的店铺。
Ashamed of having acted so rashly, Dimitri apologized to Aleko for having accused him. Dimitri羞愧于自己的鲁莽行为，为自己刚刚谴责Aleko向他道歉。
}

◊h3{独立主格结构}

◊p{(with)+n.+非谓语(doing/done/to do)}

◊lst{
ul
动词不定式要用主动表被动。
being常省略。
}

◊p{例如[NCE4.48]leave the Las Vegas mentality to those with money to fritter把拉斯维加斯式的心态留给那些有钱可供挥霍的人}

◊p{在这里}

◊lst{
ul
mentality[n]某种错误的心态或思维方式。I can't understand the mentality of the people who are behind this kind of violence.我无法理解支持这种暴力的人的心态。
fritter[v]浪费。~ ◊u{away sth/sth away} on。He frittered away a fortune on fast cars and gambling.他在快车和赌博上浪费了一大笔钱。
}

◊p{下面两个句子又有着不同的侧重点。}

◊lst{
ul
◊u{He/With him} stopping running sweat streamed down his face.
He stopped running (with) sweat streaming down his face.
}

◊h2{定语从句省略}

◊p{定语从句关系代词作主语后跟be动词，二者可一同省略。例如NCE4.1}

◊lst{
ul
the first people who were similar to ...
the first people similar to ...
}

◊h2{(even) if引导让步状从}

例如◊b{He is seldom, if ever, absent from work.他——即使有也很少——旷工。}

◊h2{虚拟语气}

◊p{对现在虚拟：◊i{If S were/did ..., S would/should/could/might do ...}}

◊p{例如◊i{Were it not for your support, I would be in big trouble now.}}

◊p{在此例句中，除虚拟语气外，还出现了其倒装形式。}

◊p{◊i{if it were not for ...}表示”若没有...的话“，因此可以译为“若没有你的支持，想必我现在已经麻烦大了罢。”}

◊p{此外，◊u{as if}/◊u{as though}◊b{可能}需要用虚拟语气。}

◊lst{
ul
Don't act as if you were the only pebble on the beach.不要表现得好像你是沙滩上唯一一块鹅卵石。}

◊p{if only要是（虚拟语气同if）}

◊lst{
ul
[NCE4.6]...if only the common peoples of the world could meet one another at football or cricket, they would have no inclination to meet on the battlefield.要是世界上主要的民族能彼此在足球或板球场上相遇，他们就不会想着在战场上相遇了。
}

◊p{辨析：only if只有...才}

◊lst{
ul
部分倒装Only if Peter goes will she go.
}

◊h2{并列句省略}

◊p{并列句中重复部分除动词搭配的介词（含动词不定式的to）外，只要不引起歧义均可省略。}

◊lst{
ul
Paul likes poetry, and Peter (likes) fiction.
Paul likes poetry, and I know that Peter likes fiction.
}

◊p{另外需注意，分号连接的常为并列句}

◊h2{the 比较级，the 比较级}

◊p{主/从句中主语非代词且谓语为be动词时be动词可省略}

◊p{如，◊i{The younger the child (is), the more (is) the child's need of sleep.}前一个分句为从句，后一个为主句；由于主语太长主句出现完全到装}

◊p{主从句语序还可以颠倒}

◊p{如◊i{I play (the) better, the more I practice.}}

◊p{又如◊i{A compound is considered the more stable, the smaller its potential energy.}}

◊p{又如[NCE3.55]◊i{... the dust becomes thinner the further we travel towards the outer edges of our own solar system.}}

◊h2{同位语}

◊p{[n]和[adj]均可用作同位语}

◊p{如[NCE4.3]◊i{Invariably the background was the same: dirt and poverty, and very uncomfortable}}

◊p{invariably相当于always；这句话翻译为“无论住在哪情况都一样：肮脏、贫穷、极度不舒适”}

◊h2{状从省略}

◊p{条件：主从句主语统一且状从谓语含be动词，即可省略状从主语和be动词}

◊lst{
ul
[NCE4.4]◊i{..., except when (she was) blindfold she lacked the ability to perceive things with her skin}除非是被遮住双眼，她缺乏用皮肤感知事物的能力
◊i{Metals expand when (they are) heated and contract when (they are) cooled}金属受热膨胀，遇冷收缩}

◊p{状语从句主语为虚主语it且谓语含be动词也可省略◊u{it be}}

◊p{◊i{We'll resolve the Taiwan issue with peaceful means when (it is) ever possible, with force when (it is) necessary}任何时候只要条件允许我们会通过和平手段解决台湾问题，而必要时我们会使用武力}

