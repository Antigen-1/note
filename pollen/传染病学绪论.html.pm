◊h1{传染病学绪论}

◊h2{感染过程中五种表现}

◊lst{ul
病原体被清除
隐性感染又称亚临床感染，是指病原体侵入人体后，仅诱导机体产生特异性免疫应答，而不引起或只引起轻微的组织损伤，因而在临床上不显出任何症状、体征甚至生化改变，只能通过免疫学检查才能发现
显性感染又称临床感染，是指病原体侵入人体后，不但诱导机体发生免疫应答，而且，通过病原体本身的作用或机体的变态反应，导致组织损伤，引起病理改变和临床表现
病原携带状态是指病原体侵入人体后，可以停留在入侵部位，或侵入较远的脏器继续生长繁殖，而人体不出现任何的疾病状态，但能携带并排除病原体，成为传染病流行的传染源
潜伏性感染又称潜在性感染，病原体感染人体后，寄生于某些部位，由于机体免疫功能足以将病原体局限化而不引起显性感染，但又不足以将病原体清除时，病原体便可长期潜伏起来，待机体免疫功能下降时，则可引起显性感染}

