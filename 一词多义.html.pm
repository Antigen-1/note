◊h1{一词多义}

◊h2{owe}

◊p{owe原意为欠。例如◊b{IOU欠条}、◊b{-How much do I owe you?(付款时用) -It's on the house.店家请客}、◊b{You owe him an apology/explanation你欠他一个道歉/解释}}

◊p{引申为感谢。如}

◊lst{
ul
I owe you (one).
I owe you a lot/a great deal.
I owe a lot to my father.
}

◊p{owe A to B表示把A归因于B（更多替换见常用替换）。}

◊h2{authority}

◊p{authority on某方面的权威}

◊p{例如，◊i{Yuan Longping is a leading authority on hybrid rice.}袁隆平是杂交水稻方面首屈一指的权威。}

◊p{还可表示当局：}

◊lst{
ul
the authorities
the local authorities
the forest authorities
the French/Chinese authorities}

◊h2{find one's way}

◊lst{ul
人作主语：找到道路
物作主语：到达}

◊p{如[NCE3.15]◊i{Very few of the fifty pence pieces and pound coins I have given him have found their way there}我给他的五十便士和一磅的硬币很少有能到达那儿的}

◊h2{promise to do}

◊lst{ul
人作主语：承诺
物作主语：可能}

◊p{如◊i{Groundbreaking research by two French geophysicists promises to shed some light on the mystery}两位法国地球物理学家的开创性研究可能揭开这一谜题}

◊h2{course}

◊p{课程，疗程，航线，道（菜）}

◊p{[NCE4.3]◊i{... eating seven-course dinners ...}一顿饭七道菜}

