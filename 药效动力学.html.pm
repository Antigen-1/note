◊h3{质反应重要概念}

◊p{ED50：半数有效量，即能引起50%的实验动物出现阳性反应时的药物剂量；如效应为死亡，则称为半数致死量（LD50）。}

◊p{LD50/ED50称为治疗指数，用于表示药物的安全性（治疗窗口）}

◊p{安全界值：LD5/ED95；可靠安全系数：LD1/ED99}

◊h2{受体}

药物受体相互作用的特征：
◊lst{ol
饱和性->竞争性
特异性（基本特性）
构效关系
信号转导机制}

药物与受体的作用既要有亲和力又要有内在活性。据此可分为：
◊lst{ul
激动药agonist◊lst{ul
完全激动药
部分激动药
反向激动药（引起与激动药相反的效应）}
拮抗药antagonist◊lst{ul
竞争性拮抗药
非竞争性拮抗药}}

受体可分为如下几类：
◊lst{ul
G蛋白偶联受体（肾上腺素受体）
配体门控离子通道受体（N型胆碱受体）
酪胺酸激酶受体（胰岛素受体）
细胞内受体（糖皮质激素受体，甲状腺激素受体、甾体激素）}

受体调节（与配体作用过程中数目和亲和力的变化）：
◊lst{ul
受体脱敏：长期使用一种激动药之后组织或细胞对激动药敏感性和反应性下降
受体增敏：与受体脱敏相反，可因受体激动药水平降低或长期应用拮抗药造成
受体脱敏和增敏若只涉及受体密度变化，则称为上调或下调}

