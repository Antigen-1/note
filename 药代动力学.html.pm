◊h2{药物的转运}

◊b{重点：脂溶扩散、离子障、pH对转运的影响。}

转运最基本的是跨膜转运（包括膜动转运即胞吞胞吐）。

绝大多数药物是通过简单扩散，尤其是脂溶性的扩散进行转运的。

给药部位浓度最高，再扩散至作用部位。

影响脂溶扩散的主要因素是脂/水分配系数、浓度梯度、解离度，此外还有膜面积等。

脂溶扩散存在◊b{离子障}：非离子型药物可以自由穿透生物膜，离子型则被限制在膜一侧。

这是由离子型极性高，水溶性高导致的。

pH值常可改变离子型和非离子型的比例，从而影响其转运。

体内pH值改变不大，故解离度影响药物转运。

