例如，弱酸性药物中毒时，应碱化尿液，抑制其重吸收。

◊b{常识：胃液、乳汁、尿液正常情况下均偏酸。}

◊(br)

注射给药时，药物直接在细胞间隙扩散，此时主要为水溶性扩散，油剂反而扩散效率慢。

