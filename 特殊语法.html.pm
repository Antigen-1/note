◊p{对现在虚拟：◊i{If S were/did ..., S would/should/could/might do ...}}

◊p{例如◊i{Were it not for your support, I would be in big trouble now.}}

◊p{在此例句中，除虚拟语气外，还出现了其倒装形式。}

◊p{◊i{if it were not for ...}表示”若没有...的话“，因此可以译为“若没有你的支持，想必我现在已经麻烦大了罢。”}

◊h2{并列句省略}

◊p{并列句中重复部分除动词搭配的介词外，只要不引起歧义均可省略。}

◊lst{
ul
Paul likes poetry, and Peter (likes) fiction.
Paul likes poetry, and I know that Peter likes fiction.
}

◊h2{the 比较级，the 比较级}

◊p{主/从句中主语非代词且谓语为be动词时be动词可省略}

◊p{如，◊i{The younger the child (is), the more (is) the child's need of sleep.}前一个分句为从句，后一个为主句；由于主语太长主句出现完全到装}

◊p{主从句语序还可以颠倒}

◊p{如◊i{I play (the) better, the more I practice.}}

◊p{又如◊i{A compound is considered the more stable, the smaller its potential energy.}}

◊p{又如[NCE3.55]◊i{... the dust becomes thinner the further we travel towards the outer edges of our own solar system.}}

◊h2{同位语}

◊p{[n]和[adj]均可用作同位语}

◊p{如[NCE4.3]◊i{Invariably the background was the same: dirt and poverty, and very uncomfortable}}

◊p{invariably相当于always；这句话翻译为“无论住在哪情况都一样：肮脏、贫穷、极度不舒适”}

